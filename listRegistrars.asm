;lista de registradores

;EAX: Acumulador para operações aritméticas.
;EBX: Base para acesso a dados.
;ECX: Contador para loops.
;EDX: Dados e I/O.
;ESI: Índice de origem para operações de string.
;EDI: Índice de destino para operações de string.
;EBP: Base Pointer para stack frames.
;ESP: Stack Pointer para controle de stack.


;RAX, RBX, RCX, RDX: Extensões de 64 bits dos registradores
